#!/bin/bash

mkdir outputs
INPUT_DIR="inputs/"
OUTPUT_DIR="outputs/"
FILE_TYPE=".txt"

CMD="grep Project"
INPUT_FILE1="gb"
OUTPUT_FILE="grep-gb"
PY_COMB="python s_grep.py"
cat "${INPUT_DIR}${INPUT_FILE1}${FILE_TYPE}" | $CMD > "${OUTPUT_DIR}${OUTPUT_FILE}-seq${FILE_TYPE}"
cat "${INPUT_DIR}${INPUT_FILE1}-0${FILE_TYPE}" | $CMD > "${OUTPUT_DIR}${OUTPUT_FILE}-0${FILE_TYPE}"
cat "${INPUT_DIR}${INPUT_FILE1}-1${FILE_TYPE}" | $CMD > "${OUTPUT_DIR}${OUTPUT_FILE}-1${FILE_TYPE}"
